"""
scripts/seed_mcp_memory.py
~~~~~~~~~~~~~~~~~~~~~~~~~~~
One-time seed of the MCP memory knowledge graph for the elibrary project.

Reads cluster_summaries.json and self_citations.csv and writes a JSONL
knowledge graph file that the MCP memory server reads on startup.

The graph captures:
  - Project entity (workspace, library size, venv)
  - Pipeline steps (scripts → outputs, completion status)
  - Cluster entities (labels, sizes, notes, self-citation counts)
  - Key findings (observations on a Findings entity)
  - Key file paths (entities with type "file")
  - Relations linking all of the above

After running this script, the MCP server will have the full project
context available as a queryable knowledge graph in Copilot Chat.

Usage:
    python scripts/seed_mcp_memory.py [--dry-run]

Flags:
    --dry-run   Print the JSONL lines without writing the file.

Author: Gerry Stahl
Created: March 2026
"""

