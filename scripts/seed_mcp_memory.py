def records_to_jsonl(records: list[dict]) -> str:
    """Convert graph records to the JSONL format the MCP server expects."""
    # The MCP server stores entities and relations as separate JSONL lines.
    # Entities format: {"type": "entity", "name": ..., "entityType": ..., "observations": [...]}
    # Relations format: {"type": "relation", "from": ..., "to": ..., "relationType": ...}
    lines = []
    for r in records:
        lines.append(json.dumps(r, ensure_ascii=False))
    return "\n".join(lines) + "\n"


def main() -> None:
    parser = argparse.ArgumentParser(description="Seed the MCP memory knowledge graph.")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print JSONL to stdout without writing file")
    args = parser.parse_args()

    print("Building knowledge graph…")
    records = build_graph()

    entities  = sum(1 for r in records if r["type"] == "entity")
    relations = sum(1 for r in records if r["type"] == "relation")
    print(f"  {entities} entities, {relations} relations")

    jsonl = records_to_jsonl(records)

    if args.dry_run:
        print("\n── JSONL preview (first 5 records) ─────────────────")
        for line in jsonl.splitlines()[:5]:
            print(line)
        print("…")
        return

    MEMORY_DIR.mkdir(exist_ok=True)
    OUT.write_text(jsonl)
    print(f"Saved: {OUT}")
    print()
    print("Next steps:")
    print("  1. VS Code will auto-detect the MCP server via .vscode/mcp.json")
    print("     (reload the window or run 'MCP: List Servers' if needed)")
    print("  2. Trust the server when VS Code prompts")
    print("  3. In Copilot Chat, the server's tools are now available:")
    print("     search_nodes, open_nodes, read_graph,")
    print("     create_entities, add_observations, create_relations")


if __name__ == "__main__":
    main()
